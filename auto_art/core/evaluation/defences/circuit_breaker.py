"""
Circuit breaker and automated rollback mechanism for autonomous agents.

Implements monitoring and automatic intervention when an agent's behavior
deviates from expected patterns, including:
- Error rate spike detection
- Token usage anomaly detection
- Action frequency monitoring
- Automated API access severance
- Rollback to last known trusted state
"""

from typing import Any, Callable, Dict, List, Optional
from dataclasses import dataclass, field
from enum import Enum
from collections import deque
import logging
import time
import copy


class CircuitState(Enum):
    """State of the circuit breaker."""
    CLOSED = "closed"
    OPEN = "open"
    HALF_OPEN = "half_open"


class AlertSeverity(Enum):
    """Severity level for circuit breaker alerts."""
    INFO = "info"
    WARNING = "warning"
    CRITICAL = "critical"
    EMERGENCY = "emergency"


@dataclass
class AgentStateSnapshot:
    """Immutable snapshot of agent state for rollback."""
    snapshot_id: str
    timestamp: float
    prompt_version: str
    rag_db_version: str
    agent_config: Dict[str, Any] = field(default_factory=dict)
    memory_state: Any = None
    metadata: Dict[str, Any] = field(default_factory=dict)


@dataclass
class CircuitBreakerAlert:
    """Alert generated by the circuit breaker."""
    timestamp: float
    severity: AlertSeverity
    metric_name: str
    current_value: float
    threshold: float
    message: str
    recommended_action: str


@dataclass
class CircuitBreakerConfig:
    """Configuration for the circuit breaker."""
    error_rate_threshold: float = 0.15
    token_usage_spike_factor: float = 3.0
    action_frequency_threshold: int = 100
    monitoring_window_seconds: float = 60.0
    half_open_max_requests: int = 5
    cooldown_seconds: float = 30.0
    max_snapshots: int = 10
    auto_rollback: bool = True


class MetricsWindow:
    """Sliding window for metric collection."""

    def __init__(self, window_seconds: float = 60.0):
        self.window_seconds = window_seconds
        self._events: deque = deque()

    def record(self, value: float, label: str = "default") -> None:
        now = time.time()
        self._events.append((now, value, label))
        self._evict_old(now)

    def _evict_old(self, now: float) -> None:
        cutoff = now - self.window_seconds
        while self._events and self._events[0][0] < cutoff:
            self._events.popleft()

    @property
    def values(self) -> List[float]:
        self._evict_old(time.time())
        return [v for _, v, _ in self._events]

    @property
    def count(self) -> int:
        self._evict_old(time.time())
        return len(self._events)

    def error_rate(self) -> float:
        self._evict_old(time.time())
        if not self._events:
            return 0.0
        errors = sum(1 for _, v, _ in self._events if v > 0)
        return errors / len(self._events)

    def mean(self) -> float:
        vals = self.values
        return sum(vals) / len(vals) if vals else 0.0


class CircuitBreaker:
    """Circuit breaker for autonomous agent safety.

    Monitors agent behavior metrics in real time and automatically trips
    when anomalous patterns are detected, severing the agent's API access
    and rolling back to the last known trusted state.
    """

    def __init__(
        self,
        config: Optional[CircuitBreakerConfig] = None,
        on_trip: Optional[Callable[[CircuitBreakerAlert], None]] = None,
        on_recover: Optional[Callable[[], None]] = None,
    ):
        self.logger = logging.getLogger("auto_art.circuit_breaker")
        self.config = config or CircuitBreakerConfig()
        self.on_trip = on_trip
        self.on_recover = on_recover

        self._state = CircuitState.CLOSED
        self._error_window = MetricsWindow(self.config.monitoring_window_seconds)
        self._token_window = MetricsWindow(self.config.monitoring_window_seconds)
        self._action_window = MetricsWindow(self.config.monitoring_window_seconds)
        self._half_open_requests = 0
        self._last_trip_time: float = 0.0

        self._snapshots: List[AgentStateSnapshot] = []
        self._alerts: List[CircuitBreakerAlert] = []
        self._baseline_token_usage: float = 0.0

    @property
    def state(self) -> CircuitState:
        if self._state == CircuitState.OPEN:
            elapsed = time.time() - self._last_trip_time
            if elapsed >= self.config.cooldown_seconds:
                self._state = CircuitState.HALF_OPEN
                self._half_open_requests = 0
                self.logger.info("Circuit breaker entering HALF_OPEN state.")
        return self._state

    @property
    def is_allowing_requests(self) -> bool:
        state = self.state
        if state == CircuitState.CLOSED:
            return True
        if state == CircuitState.HALF_OPEN:
            return self._half_open_requests < self.config.half_open_max_requests
        return False

    def save_snapshot(self, snapshot: AgentStateSnapshot) -> None:
        """Save a trusted state snapshot for potential rollback."""
        self._snapshots.append(snapshot)
        if len(self._snapshots) > self.config.max_snapshots:
            self._snapshots.pop(0)
        self.logger.info(f"Saved state snapshot: {snapshot.snapshot_id}")

    def get_latest_snapshot(self) -> Optional[AgentStateSnapshot]:
        """Get the most recent trusted state snapshot."""
        return self._snapshots[-1] if self._snapshots else None

    def record_request(self, is_error: bool = False, token_count: int = 0) -> Optional[CircuitBreakerAlert]:
        """Record an agent request and check for anomalies.

        Args:
            is_error: Whether the request resulted in an error.
            token_count: Number of tokens consumed by this request.

        Returns:
            CircuitBreakerAlert if the circuit was tripped, None otherwise.
        """
        self._error_window.record(1.0 if is_error else 0.0)
        self._token_window.record(float(token_count))
        self._action_window.record(1.0)

        if self._state == CircuitState.HALF_OPEN:
            self._half_open_requests += 1
            if is_error:
                return self._trip("error_rate_half_open", 1.0, 0.0,
                                  "Error during HALF_OPEN probe; re-opening circuit.")
            if self._half_open_requests >= self.config.half_open_max_requests:
                self._recover()
                return None

        error_rate = self._error_window.error_rate()
        if error_rate > self.config.error_rate_threshold:
            return self._trip(
                "error_rate", error_rate, self.config.error_rate_threshold,
                f"Error rate {error_rate:.2%} exceeds threshold {self.config.error_rate_threshold:.2%}",
            )

        if self._baseline_token_usage > 0:
            current_mean = self._token_window.mean()
            if current_mean > self._baseline_token_usage * self.config.token_usage_spike_factor:
                return self._trip(
                    "token_usage_spike", current_mean,
                    self._baseline_token_usage * self.config.token_usage_spike_factor,
                    f"Token usage {current_mean:.0f} exceeds {self.config.token_usage_spike_factor}x baseline",
                )

        action_count = self._action_window.count
        if action_count > self.config.action_frequency_threshold:
            return self._trip(
                "action_frequency", float(action_count),
                float(self.config.action_frequency_threshold),
                f"Action frequency {action_count} exceeds threshold in window",
            )

        return None

    def set_baseline_token_usage(self, baseline: float) -> None:
        """Set the baseline average token usage for spike detection."""
        self._baseline_token_usage = baseline
        self.logger.info(f"Baseline token usage set to {baseline:.0f}")

    def _trip(
        self, metric: str, value: float, threshold: float, message: str,
    ) -> CircuitBreakerAlert:
        """Trip the circuit breaker."""
        self._state = CircuitState.OPEN
        self._last_trip_time = time.time()

        severity = AlertSeverity.CRITICAL
        recommended = "Rollback to last trusted snapshot and investigate."

        alert = CircuitBreakerAlert(
            timestamp=time.time(),
            severity=severity,
            metric_name=metric,
            current_value=value,
            threshold=threshold,
            message=message,
            recommended_action=recommended,
        )
        self._alerts.append(alert)
        self.logger.critical(f"CIRCUIT BREAKER TRIPPED: {message}")

        if self.on_trip:
            self.on_trip(alert)

        return alert

    def _recover(self) -> None:
        """Recover the circuit breaker to CLOSED state."""
        self._state = CircuitState.CLOSED
        self.logger.info("Circuit breaker recovered to CLOSED state.")
        if self.on_recover:
            self.on_recover()

    def force_open(self, reason: str = "manual") -> CircuitBreakerAlert:
        """Manually trip the circuit breaker."""
        return self._trip("manual_override", 0.0, 0.0, f"Manual circuit break: {reason}")

    def force_close(self) -> None:
        """Manually close the circuit breaker."""
        self._state = CircuitState.CLOSED
        self.logger.info("Circuit breaker manually closed.")

    def get_alerts(self, limit: int = 50) -> List[CircuitBreakerAlert]:
        """Get recent alerts."""
        return self._alerts[-limit:]

    def get_status(self) -> Dict[str, Any]:
        """Get current circuit breaker status."""
        return {
            "state": self.state.value,
            "is_allowing_requests": self.is_allowing_requests,
            "error_rate": self._error_window.error_rate(),
            "action_count": self._action_window.count,
            "token_usage_mean": self._token_window.mean(),
            "baseline_token_usage": self._baseline_token_usage,
            "snapshots_count": len(self._snapshots),
            "alerts_count": len(self._alerts),
        }

    def rollback(self, agent: Any) -> Optional[AgentStateSnapshot]:
        """Rollback the agent to the last trusted state snapshot.

        Args:
            agent: The agent instance. Must support set_config() or
                   equivalent state restoration methods.

        Returns:
            The snapshot rolled back to, or None if no snapshots available.
        """
        snapshot = self.get_latest_snapshot()
        if snapshot is None:
            self.logger.error("No snapshots available for rollback.")
            return None

        self.logger.info(f"Rolling back to snapshot: {snapshot.snapshot_id}")

        if hasattr(agent, "set_config"):
            agent.set_config(snapshot.agent_config)
        if hasattr(agent, "set_memory") and snapshot.memory_state is not None:
            agent.set_memory(copy.deepcopy(snapshot.memory_state))
        if hasattr(agent, "set_prompt_version"):
            agent.set_prompt_version(snapshot.prompt_version)

        return snapshot
