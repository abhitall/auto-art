"""
ART Adversarial Training defence wrappers.

Wraps ART's adversarial training defences (Madry PGD training,
AdversarialTrainerFBF) into the auto-art DefenceStrategy interface.

Reference: ART Defences Wiki - Trainer section
"""

from typing import Any, Dict, Optional
import logging

from .base import DefenceStrategy

logger = logging.getLogger(__name__)

try:
    from art.defences.trainer import (
        AdversarialTrainerMadryPGD as ARTAdversarialTrainerMadryPGD,
    )
    ART_TRAINERS_AVAILABLE = True
except ImportError:
    ART_TRAINERS_AVAILABLE = False


class AdversarialTrainingPGDDefence(DefenceStrategy):
    """PGD-based Adversarial Training defence (Madry et al., 2017).

    Trains the model with adversarial examples generated by PGD,
    improving robustness against gradient-based evasion attacks.
    This is the strongest standard adversarial training method.
    """

    def __init__(
        self,
        nb_epochs: int = 20,
        eps: float = 0.3,
        eps_step: float = 0.1,
        max_iter: int = 7,
        batch_size: int = 128,
        ratio: float = 1.0,
    ):
        super().__init__(defence_name="AdversarialTrainingPGD")
        self.nb_epochs = nb_epochs
        self.eps = eps
        self.eps_step = eps_step
        self.max_iter = max_iter
        self.batch_size = batch_size
        self.ratio = ratio
        self._trainer: Optional[Any] = None

    def apply(self, art_estimator: Any, **kwargs) -> Any:
        """Apply adversarial training to the estimator.

        Note: Unlike preprocessor defences, adversarial training requires
        training data. Pass `x_train` and `y_train` in kwargs.
        """
        if not ART_TRAINERS_AVAILABLE:
            raise ImportError("ART trainer defences not available.")

        from art.attacks.evasion import ProjectedGradientDescent

        pgd = ProjectedGradientDescent(
            estimator=art_estimator,
            eps=self.eps,
            eps_step=self.eps_step,
            max_iter=self.max_iter,
            batch_size=self.batch_size,
        )

        self._trainer = ARTAdversarialTrainerMadryPGD(
            classifier=art_estimator,
            attacks=pgd,
            ratio=self.ratio,
        )

        x_train = kwargs.get("x_train")
        y_train = kwargs.get("y_train")

        if x_train is not None and y_train is not None:
            logger.info(
                f"Running adversarial training for {self.nb_epochs} epochs "
                f"on {len(x_train)} samples."
            )
            self._trainer.fit(
                x_train, y_train,
                nb_epochs=self.nb_epochs,
                batch_size=self.batch_size,
            )
            return self._trainer.get_classifier()
        else:
            logger.warning(
                "No training data provided (x_train, y_train). "
                "Returning trainer without training."
            )
            return art_estimator

    def get_params(self) -> Dict[str, Any]:
        return {
            "nb_epochs": self.nb_epochs,
            "eps": self.eps,
            "eps_step": self.eps_step,
            "max_iter": self.max_iter,
            "batch_size": self.batch_size,
            "ratio": self.ratio,
        }
